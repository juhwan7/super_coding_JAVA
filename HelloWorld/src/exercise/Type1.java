package exercise;

public class Type1 {

    public static void main(String[] args) {
        int myFavoriteNumber = 7;
        double pi = 3.141592;
        boolean likeMint = true;
        char mySurnameFirstAlphabet = 'Y';
        String myName = "김주환";

        System.out.println("myFavoriteNumber = " + myFavoriteNumber);
        System.out.println("pi = " + pi);
        System.out.println("likeMint = " + likeMint);
        System.out.println("mySurnameFirstAlphabet = " + mySurnameFirstAlphabet);
        System.out.println("myName = " + myName);
    }
}
